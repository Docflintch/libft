#include "libft.h"

void	ft_lstclear(t_list **lst, void (*del)(void*))
{
	t_list	*temp;

	temp = *lst;
	if (temp->next)
		ft_lstclear(&(temp)->next, del);
	ft_lstdelone(temp, del);
}
